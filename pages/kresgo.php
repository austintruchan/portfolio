					<h2>Visual Design.</h2>
					<p>The process of paper prototyping made me think about how the display could be even simpler. I decided to make the home screen just a voice recognition system, much like that of Siri. <span>I thought that if I was lost in a building the easiest way to find my way around would be to ask someone.</span> So by making the design revolve around a voice recognition system, I could essentially create a digital assistant that is always available to the public. I decided to name this digital assistant "Kresgo", and to put him smack dab in the middle of the home view. I wanted him to be the first thing that people came into contact with when they walked in, allowing one to simply tap, speak, and get on with their day.</p>
					
