<!-- Portfolio filter -->
<script type="text/javascript">
$(function() {
var selectedClass = "";
$(".fil-cat").click(function(){
selectedClass = $(this).attr("data-rel");
$(".designs").fadeTo(100, 0.1);
$(".designs ul li").not("."+selectedClass).fadeOut().removeClass('scale-anm');
setTimeout(function() {
$("."+selectedClass).fadeIn().addClass('scale-anm');
$(".designs").fadeTo(300, 1);
}, 300);
});
});
</script>

