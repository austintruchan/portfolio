                <p>Working with MSU Out of the Box (A group of multimedia students focused on rebranding non-profits), We will actually be rebranding and implementing the re-design for the nonprofit: The Disabled Traveler in early 2015. We will be creating a new website, logo and social media pages for them so that their information on handicap accessibility can be easily accessed by the public. Besides the rebranding, we’re heading to Zion and Bryce national parks over spring break to document each park’s accessibility features. You can learn more at <a href="http://msuoutofthebox.com" target="_blank">msuoutofthebox.com</a>. <span>Acting as lead designer for the team, I envisioned a new, refreshed, and modern re-design for the Disabled Traveler, that will ultimately make accesbility information for National Parks more accessible than ever before.</span> Through the logo, the new brand, and the new web design, the Disabled Traveler will truly be seen as the forefront of disabled traveling within national parks and beyond. <span>Below you can see the refreshed brand guide, logo, and type treatment.</span></p></div>

