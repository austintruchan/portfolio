            <div class="design-single content">
                <h3>Web Design</h3>
                <p> The new website had to be as simple, clean and intuitive as possible. <span>I want the user to be able to quickly and naturally find and view accessbility information on National Parks without even having to think about it.</span> I  removed all of the clutter that was available on their old site, and broke the new site down to only include the main functionality of finding and viewing acessibility information. The site also focuses immensely on large, engaging imagery, in order to inspire it's users to visit the associated National Park. I also included interactive maps powered by google, to visually allow the user to see the attractions and trails for each national park. You can see the visual design for the new website below.</p></div>

