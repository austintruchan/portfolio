                <p><span>The Disabled Traveler is the principal resource for disabled traveling in national parks and beyond.</span>  They are a non-profit located in Lapeer, MI, and their goal is to remove much of the unknown in our national parks that concerns visitors with a physical disability or age related difficulties, so that they may have the best experience possible. They accomplish this by photographing national parks and their disability access, as well as gathering facts and statistics about each park's handicap accessibility features. This information can be accessed through their website, <a href="http://tdtcompanion.com" target="_blank">tdtcompanion.com</a> (Check out their current website to view their current brand, logo, and web design). Finding out accessibility information on national parks is a very difficult process—the Disabled Traveler strives to bring this information together in one easy-to-use place, making accessibility information, well, accessible. Right now, the Disabled Traveler's website is a bit outdated. I wanted to refresh their brand and build up their website so that this important information is simply laid out and easy to understand. <span>Below you can see their current website design, and brand.</span></p></div>

