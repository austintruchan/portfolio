                <p>The redesign began with a moodboard featuring a new conceptual visual design for the new brand. I focused immensely on positive traveling experiences, powerful imagery featuring paths through nature, authentic font choices, and modern, yet natural color options. <span>The vision behind the new brand would ultimately provide users with a sense of ease, comfort, and accessible travel experiences, and inspire them to visit the beautiful national parks the U.S. has to offer.</span></p></div>

