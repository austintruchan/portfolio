                       <h4>Philosophy</h4></div></div>

              <div class="design-single content">
                <p><span>Experience.</span> I design for the experience. The experience always comes first. Something can look beautiful, but if it doesn't work as it should, it's bad design. I strive for design that works, that is natural and that is delightful. Visual aesthetics are incredibly important, but will always come second. UX, then UI. Wireframes, then Design Comps. </p><div class="break"></div><p><span> Simplicity.</span> I want those who use my designs to already know how to use them before they've ever even heard of them. My design simply needs to be a natural extension of the individual. They must be easy to understand. They have to be simple, and sensible. Eliminate the unnecessary, repetitive distractions. Break it down to it's core fundamentals. Make interactive elements noticeable. Create Hierarchy. Create a natural flow, a sense of direction and completion. Create a story.</p><div class="break"></div><p><span> Aesthetics.</span> When a design is beautiful, elegant and sophisticated, the user experience is enhanced immensely. It's so refreshing to experience an attractive, clean interface that makes you want to come back to experience it again. The way it looks, the way it moves, the way it interacts, it has to be beautiful.</p></div>

