             <h3>USER RESEARCH</h3>
                <p>According to Pew Research, Some 53% of American adults age 18 and older play video games, and 21% play everyday or almost everyday. They allow an individual to escape from reality, and transcend into a digital realm away from all stresses. There are many who are addicted to this form of mental diversion. So much so that their real lives become their second reality, and their digital lives become their first. An addiction to video games can cause people to become lazy, irresponsible and unproductive. <span>I shadowed my friend Craig for a whole day and tracked his video game gameplay stats, which you can see in the graphic below.</span> Craig is a Junior at MSU studying Computer Engineering. His favorite past time is playing video games. Craig is motivated to play video games because "I want to beat them and to be able to feel awesome." Craig's end goals towards video games are "to kill the time, and to be sucked into a world that I can control." Throughout the day, Craig only took breaks to eat, and watch the redwings game. He did not do his homework and failed to do his weekend chores. You can view Craig's gameplay chart below. Craig is clearly addicted to video games, and because of it, is unproductive. So I thought, <span>what if there was an app, much like a workout routine app, that was designed for video game addicts, but instead of becoming fit, they would become un-addicted to video games?</span></p></div>

