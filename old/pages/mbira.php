             <h3>Mbira Plugin - Wireframes</h3>
             <p> The goal of mbira is to create a constellation of open source tools (existing and purpose built) that will lower the technical bar for individuals, projects, and institutions interested in building rich, engaging, and sustainable place-based and mobile heritage experiences. <span>Therefore the plugin admin interface needed to be as simple and intuitive as possible.</span> I wanted to create a natural experience for admins that required little to no thought when creating new projects, new exhibits, locations, etc., making the process of building, managing, and sustaining a mobile heritage experience as easy as possible.</p></div>

