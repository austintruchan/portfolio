            <p><span>The visual design of mbira, takes colors from it's logo, as well as this concept of modern, flat, and aesthetically pleasing interface design.</span> The colors of mbira provide a sense of ease, peace and simplicity. The accent blue works well with the other shades of blue to help users notice significant interactive elements. The plugin also takes advantage of transparent elements in order to create a sense of depth, and allow imagery to pop through, ultimately engaging the user further.</p>   </div>

