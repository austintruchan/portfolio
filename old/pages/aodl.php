            <div class="design-single content">
                <h3>Process</h3>
                <p>After completion of an <a href="#aodl-20">user scenario</a>, we sketched out our ideas and moved to digital wireframing. We wanted to create an engaging user experience, that did more than just present 15 different links. <span>We wanted to create an interactive narrative that inspired users to explore deeper into the content, making researching on african multimedia as compelling and interesting as possible.</span> We wanted to create a visual experience that didn't just give the user the information they were looking for, but encouraged them to come back for more.</p>
