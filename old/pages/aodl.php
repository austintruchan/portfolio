             <h3>AODL?</h3>
             <p>AODL is a portal to multimedia collections about Africa. MATRIX, working in cooperation with the African Studies Center at Michigan State University, is partnering with universities and cultural heritage organizations in Africa to build this resource. AODL contains 15 external weblinks that simply take you to other websites on African Resources. That's all it is. <span>How can you transform a website that barely has any content into something interactive, meaningful and compelling?</span> We began by conducting a landscape analysis of other online digital libraries and popular websites and found new ways to organize the information of AODL. Netflix was a huge inspiration for use, which inspired us to add a featured section, and create a perfect grid with the web links of AODL. <span> The redesign will be implemented later this year, but you can see what AODL looks like today, and what we had to redesign below.</span></p></div>

