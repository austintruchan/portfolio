            <div class="design-single content">
                <h3>Process</h3>
                <p>The project began by doing a brief landscape analysis of other popular kinetic brand systems and then moving on to a process of morphological thinking, focusing on many options of elements that could make up my unique brand identity. <span>I mapped out what East Lansing 2030: CollegeVille Re-Envisioned would look like in over 100 different font options, played around with tons of different color options, and expiremented with imagery combination and collage.</span> Throughout the process, I continued to develop the system with a futuristic, edgy, and expiremental approach, trying my best to represent what graphic style may look like in the year 2030. My final solutions, are meant to represent a sort of disrupted utopia, as I use healthy green color variations to represent growth and thriving civilization, and glitchy, edgy, and disrupted imagery to represent future graphic trends. You can see the general progression of my process below.</p></div>

