                <h3>Poster</h3>
                <p>The kinetic system was first applied to a 16inx30in poster. The dimensions of the poster were chosen to work with the concepts futuristic, edgy and expiremental graphic communication. Through the poster, I wanted to create a thriving, active piece that could easily catch the eye. I focused immensely on Hierarchy in order to guide the user through the piece, feeding them information in an easy to consume fashion. I also pushed for a great sense of depth within the poster, in order to engage the user visually. </p></div>

